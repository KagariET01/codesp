template<typename I>struct triple{//    a/b
	I a=0,b=1;
	triple fix(){
		I gcd=__gcd(a,b);
		a/=gcd;
		b/=gcd;
		return *this;
	}
	triple(I x){
		a=x;
		b=1;
	}
	triple(I x,I y){
		a=x;
		b=y;
	}
};

template<typename I>triple<I> operator+(triple<I> a,I b){
	return triple(a.a+a.b*b,a.b).fix();
}







//  圖論

template<typename I>struct pnt{
	I x,y;
	pair<I,I>&operator=(pair<I,I> b){
		x=b.F;
		y=b.S;
		return b;
	}
	pnt(I a,I b){
		x=a,y=b;
	}
};


template<typename I>ostream&operator<<(ostream&ou,pnt<I> a){
	return ou<<"("<<a.x<<","<<a.y<<")"<<endl;
}
template<typename I>istream&operator>>(istream&in,pnt<I> a){
	return in>>a.x>>a.y;
}

template<typename I>pnt<I> operator+(pnt<I>&a,pnt<I>&b){// add
	return pnt<I>(a.x+b.x,a.y+b.y);
}

template<typename I>pnt<I> operator-(pnt<I>&a,pnt<I>&b){// subtract
	return pnt<I>(a.x-b.x,a.y-b.y);
}



template<typename I>I operator*(pnt<I>&a,pnt<I>&b){// dot
	// = |a| |b| cos(θ)
	return a.x*b.x + a.y*b.y;
}

template<typename I>I operator^(pnt<I>&a,pnt<I>&b){// cross
	// = |a| |b| sin(θ)
	// >0 Counterclockwise
	// <0 Clockwise
	return a.x*b.y - a.y*b.x;
}

