INT mx[]={0,1,0,-1};
INT my[]={1,0,-1,0};

int main(){
	cin.tie(0);cout.tie(0);ios::sync_with_stdio(0);
	INT n,m;
	cin>>n>>m;
	string str[n];
	for(auto&i:str)cin>>i;
	INT mp[n][m]={};
	//0=can move
	//1=end here
	//2=wall dont move

	for(INT i=0;i<n;i++){
		for(INT j=0;j<m;j++){
			if(str[i][j]=='#'){
				mp[i][j]=2;
				for(INT k=0;k<4;k++){
					INT nx=i+mx[k];
					INT ny=j+my[k];
					if(nx<0||n<=nx||ny<0||m<=ny)continue;
					maxs(mp[nx][ny],1ll);
				}
			}
		}
	}
	bool mv[n][m]={};
	INT ans=0;
	for(INT i=0;i<n;i++){
		for(INT j=0;j<m;j++){
			if(mv[i][j])continue;
			if(mp[i][j]==1){maxs(ans,1ll);continue;}
			if(mp[i][j]==2)continue;
			vector<PII>vec;//record mp[x][y]==1
			deque<PII>dq;
			INT cnt=0;
			dq.push_back(PII(i,j));
			mv[i][j]=1;
			while(!dq.empty()){
				INT x=dq.front().F;
				INT y=dq.front().S;
				dq.pop_front();
				cnt++;
				if(mp[x][y]==1){
					vec.push_back(PII(x,y));
					continue;
				}
				for(INT k=0;k<4;k++){
					INT nx=x+mx[k];
					INT ny=y+my[k];
					if(nx<0||n<=nx||ny<0||m<=ny)continue;
					if(mv[nx][ny])continue;
					mv[nx][ny]=1;
					dq.push_back(PII(nx,ny));
				}
			}
			maxs(ans,cnt);
			for(PII&i:vec){
				mv[i.F][i.S]=0;
			}
		}
	}
	cout<<ans<<endl;
	return 0;
}



