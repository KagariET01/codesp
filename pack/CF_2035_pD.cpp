const INT MOD=1e9+7;

namespace ET01{
	INT pow(INT a,INT n,INT mod=0ll){
		if(n==0ll)return 1ll;
		INT re=a;
		INT x=a;
		n--;
		while(n>0ll){
			if(n&1)re*=x;
			x*=x;
			n>>=1ll;
			if(mod)x%=mod,re%=mod;
		}
		return re;
	}
}

